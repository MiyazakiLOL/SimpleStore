class Product {
  final String id;
  final String name;
  final String imageUrl;
  final String tag;
  final double price;
  final int sold;
  final String category;

  Product({
    required this.id,
    required this.name,
    required this.imageUrl,
    required this.tag,
    required this.price,
    required this.sold,
    required this.category,
  });
}
